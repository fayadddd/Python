# \n = new line
story = "once upon a time \n there was a millioner \n name fayad \n who get 1 miilion \n in every \n month"
print(story)



# \t = big space
story = "once upon a time \t there was a millioner \t name fayad \t who get 1 miilion \t in every \t month"
print(story)



# \' = '
story = "\'once upon a time\' there was a millioner \'name fayad\' who get 1 miilion in every \'month\'"
print(story)



# \\ = \
story = "once upon a time\\there was a millioner\\name fayad\\who get 1 miilion in every\\month"
print(story)
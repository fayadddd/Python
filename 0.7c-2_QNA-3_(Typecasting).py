

#1
a = "2435"          # ("")(string)
print(type(a))      # str
print(a+5)          # not valued

# (Str) to (Int)
a = "2435"
a = int(a)          # covert (str) into (int)
print(type(a))      # int
print(a+5)          # 2440



#2
b = 31           # (number = integer)
print(type(b))      # int

# (Int) to (Str)
b = 31           # int     
b = str(b)          # convert (int) into (str)
print(type(b))      # str



#3
c = 31.0            # 00.0 (float)          
print(type(c))      # float

# (Float) to (Int)
c = 31.0            # float
c = int(c)          # convert (float) into (int)
print(type(c))      # int



